const mongoose = require("mongoose");

const UserSchema = new mongoose.Schema({

/* ===================================================== */
/* ================= AUTH ============================= */
/* ===================================================== */

email:{
type:String,
required:true,
unique:true
},

password:{
type:String,
required:true
},

/* ===================================================== */
/* ================= STRIPE =========================== */
/* ===================================================== */

stripeCustomerId:{
type:String
},

defaultPaymentMethod:{
type:String
},

/* ===================================================== */
/* =============== PAYMENT STATUS ===================== */
/* ===================================================== */

paid:{
type:Boolean,
default:false
},

/* ===================================================== */
/* =============== CREDIT SYSTEM ====================== */
/* ===================================================== */

credits:{
type:Number,
default:0
},

/* ===================================================== */
/* =============== SUBSCRIPTION ======================= */
/* ===================================================== */

subscriptionStatus:{
type:String,
enum:["none","active","canceled"],
default:"none"
},

subscriptionId:{
type:String
},

/* ===================================================== */
/* =============== META =============================== */
/* ===================================================== */

createdAt:{
type:Date,
default:Date.now
}

});

module.exports = mongoose.model("User",UserSchema);
